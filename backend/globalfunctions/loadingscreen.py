# Colors the $ in blue
def colorize_dollars(text):
    MAGENTA = "\033[95m"  # bright magenta
    RESET = "\033[0m"
    return text.replace("$", f"{MAGENTA}${RESET}")

# Loops the animation
def loadingscreenanimation():
    for i in range(1):
        for frame in animation:
            os.system('cls' if os.name == 'nt' else 'clear')  # clear terminal
            print(colorize_dollars(frame))
            time.sleep(0.3)
    # Makes the NE as the final frame of the anim
    os.system('cls' if os.name == 'nt' else 'clear')
    print(colorize_dollars(ne))